#match case это замена if else когда нужно сравнить нужное значение с разными вариантами
#функция (def) это срабатывание определенного блока кода, когда ты ее вызываешь
def select_task():
    task = input("виберіть задавдання: 1-6 ")
    match task:
        case "1":
            task1()
        case "2":
            task2()
        case "3":
            task3()
        case "4":
            task4()
        case "5":
            task5()
        case "6":
            task6()
        case "exit":
            exit()
        case _:
            print("невірний вибір, спробуйте ще раз")
            #рекурсия - вызов функции внутри самой себя
            select_task()
