def task5():
    n1 = float(input("введіть перше число: "))
    n2 = float(input("введіть друге число: "))
    operation = input("виберіть операцію (додавання, віднімання, множення, ділення, знаходження залишку, піднесення до степеня): ")
    match operation:
        case "додавання":
            print(f"результат: {n1 + n2}")
        case "віднімання":
            print(f"результат: {n1 - n2}")
        case "множення":
            print(f"результат: {n1 * n2}")
        case "ділення":
            if n2 != 0:
                print(f"результат: {n1 / n2}")
            else:
                print("помилка: ділення на нуль")
        case "знаходження залишку":
            print(f"результат: {n1 % n2}")
        case "піднесення до степеня":
            print(f"результат: {n1 ** n2}")
        case _:
            print("невірна операція")
